/**
 * Normalizes text to check for duplicates.
 * Removes spaces, punctuation, case, and special characters.
 */
function normalizeText(text) {
  if (!text) return "";
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]/g, "")
    .trim();
}

/**
 * Split a single statement into a question and answer based on verbs or delimiters.
 */
function splitStatement(line, category) {
  // Check for explicit arrow symbol first (very common in QA style, e.g. DBMS)
  if (line.includes("→")) {
    const parts = line.split("→");
    return {
      question: parts[0].trim(),
      answer: parts[1].trim()
    };
  }
  if (line.includes("->")) {
    const parts = line.split("->");
    return {
      question: parts[0].trim(),
      answer: parts[1].trim()
    };
  }

  // Check for explicit "=" sign
  if (line.includes("=")) {
    const parts = line.split("=");
    return {
      question: parts[0].trim(),
      answer: parts[1].trim()
    };
  }

  // Find the first matching verb from our VERBS array
  const lowerLine = line.toLowerCase();
  let firstVerb = null;
  let firstIdx = Infinity;

  for (const verb of VERBS) {
    const idx = lowerLine.indexOf(verb);
    if (idx !== -1 && idx < firstIdx) {
      firstIdx = idx;
      firstVerb = verb;
    }
  }

  if (firstVerb) {
    const q = line.substring(0, firstIdx).trim();
    let a = line.substring(firstIdx).trim();
    return {
      question: q,
      answer: a
    };
  }

  // Category fallback for short statements under specific categories
  if (line.length < 35 && category && category !== "General" && category !== "Miscellaneous") {
    return {
      question: `${category}`,
      answer: line
    };
  }

  // Fallback if no verb or equals is found: split in half or return as question
  if (line.length > 50) {
    const words = line.split(" ");
    const mid = Math.floor(words.length / 2);
    return {
      question: words.slice(0, mid).join(" ").trim(),
      answer: words.slice(mid).join(" ").trim()
    };
  }

  return {
    question: line,
    answer: "Yes/True" // Default fallback
  };
}

/**
 * Parses raw text into a clean array of deduplicated facts.
 */
function parseRawSubject(subjectName, rawText) {
  const lines = rawText.split("\n").map(l => l.trim());
  const facts = [];
  const seenQuestions = new Set();

  let currentCategory = "General";

  // Noise filtering rules
  const isNoise = (line) => {
    if (!line) return true;
    if (line.includes("Printed using") || line.includes("ChatGPT to PDF") || line.includes("HTML to PDF API")) return true;
    if (/^\d+\/\d+$/.test(line)) return true; // Page numbers like "164/205"
    if (line.includes("★") || line.includes("Exam Importance")) return true;
    if (line.includes("This chapter is") || line.includes("These are high-frequency")) return true;
    if (line.includes("Super Fast Revision") || line.includes("Quick Memory Booster")) return true;
    if (line.includes("Part 2") || line.includes("Part 3") || line.includes("part 2") || line.includes("part 3")) return true;
    if (line.includes("Chapter 18") || line.includes("200+ One-Liner") || line.includes("Purpose: High-yield") || line.includes("Final revision notes")) return true;
    if (line.includes("One-Liners 71–140") || line.includes("One-Liners 141–210")) return true;
    if (/^---$/.test(line)) return true; // Separators
    if (line.includes("These 120 numerical facts") || line.includes("cover the highest-frequency")) return true;
    return false;
  };

  let idx = 0;
  while (idx < lines.length) {
    let line = lines[idx];

    if (isNoise(line)) {
      idx++;
      continue;
    }

    // Strip bold markers and clean spacing
    line = line.replace(/\*\*/g, "").replace(/\*/g, "").trim();

    // Check if line is a numbered marker on its own line: e.g. "1.", "142."
    const isIsolatedNumberMarker = /^\d+\.$/.test(line);
    // Check if line starts with a number marker followed by text: e.g. "1. 8085 is an 8-bit microprocessor."
    const inlineNumberMatch = line.match(/^(\d+)\.\s+(.*)$/);

    if (isIsolatedNumberMarker || inlineNumberMatch) {
      let contentLines = [];

      if (inlineNumberMatch) {
        // First content line is the inline text after the number
        contentLines.push(inlineNumberMatch[2].trim());
        idx++;
      } else {
        // Number is on its own line, so content lines start on next line(s)
        idx++;
      }

      // Gather subsequent lines until the next number marker, noise, or header
      while (idx < lines.length) {
        let nextLine = lines[idx];
        if (isNoise(nextLine)) {
          idx++;
          continue;
        }

        // Clean markdown
        const cleanNext = nextLine.replace(/\*\*/g, "").replace(/\*/g, "").trim();

        // Stop if we hit a number marker (isolated or inline)
        if (/^\d+\.$/.test(cleanNext) || /^(\d+)\.\s+(.*)$/.test(cleanNext)) {
          break;
        }

        // Stop if we hit a header (e.g. starts with "##" or is short non-verb text)
        const isHeader = cleanNext.startsWith("#") ||
          (cleanNext.length < 35 &&
            !VERBS.some(v => cleanNext.toLowerCase().includes(v)) &&
            !cleanNext.includes("=") &&
            !cleanNext.includes(":") &&
            !/\d/.test(cleanNext));

        if (isHeader) {
          break;
        }

        contentLines.push(cleanNext);
        idx++;
      }

      if (contentLines.length > 0) {
        let question = "";
        let answer = "";

        if (contentLines.length === 1) {
          const split = splitStatement(contentLines[0], currentCategory);
          question = split.question;
          answer = split.answer;
        } else if (contentLines.length >= 2) {
          question = contentLines[0];
          answer = contentLines.slice(1).join(" ");
        }

        // Deduplication using normalized question text
        const normQ = normalizeText(question);
        if (normQ && !seenQuestions.has(normQ)) {
          seenQuestions.add(normQ);
          facts.push({
            id: facts.length + 1,
            category: currentCategory,
            question: question,
            answer: answer
          });
        }
      }
    } else {
      // It's a header line
      // Clean markdown headers like "## "
      const cleanHeader = line.replace(/^#+\s+/, "").replace(/:$/, "").trim();
      if (cleanHeader.length < 50) {
        currentCategory = cleanHeader;
      }
      idx++;
    }
  }

  // Also parse memory boosters or inline "=" statements
  lines.forEach(line => {
    if (line.includes("=") && !isNoise(line)) {
      const cleanLine = line.replace(/\*\*/g, "").replace(/\*/g, "").trim();
      // Skip lines starting with numbers to prevent double-parsing numbered facts
      if (/^\d/.test(cleanLine)) {
        return;
      }
      const parts = cleanLine.split("=");
      const q = parts[0].trim();
      const a = parts[1].trim();
      const normQ = normalizeText(q);

      if (normQ && !seenQuestions.has(normQ)) {
        seenQuestions.add(normQ);
        facts.push({
          id: facts.length + 1,
          category: "Quick Boosters",
          question: q,
          answer: a
        });
      }
    }
  });

  return facts;
}

