/**
 * Normalizes text to check for duplicates.
 * Removes spaces, punctuation, case, and special characters.
 */
function normalizeText(text) {
  if (!text) return "";
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]/g, "")
    .trim();
}

/**
 * Split a single statement into a question and answer based on verbs or delimiters.
 */
function splitStatement(line, category) {
  // Check for explicit arrow symbol first (very common in QA style, e.g. DBMS)
  if (line.includes("→")) {
    const parts = line.split("→");
    return {
      question: parts[0].trim(),
      answer: parts[1].trim()
    };
  }
  if (line.includes("->")) {
    const parts = line.split("->");
    return {
      question: parts[0].trim(),
      answer: parts[1].trim()
    };
  }

  // Check for explicit "=" sign
  if (line.includes("=")) {
    const parts = line.split("=");
    return {
      question: parts[0].trim(),
      answer: parts[1].trim()
    };
  }

  // Find the first matching verb from our VERBS array
  const lowerLine = line.toLowerCase();
  let firstVerb = null;
  let firstIdx = Infinity;

  for (const verb of VERBS) {
    const idx = lowerLine.indexOf(verb);
    if (idx !== -1 && idx < firstIdx) {
      firstIdx = idx;
      firstVerb = verb;
    }
  }

  if (firstVerb) {
    const q = line.substring(0, firstIdx).trim();
    let a = line.substring(firstIdx).trim();
    return {
      question: q,
      answer: a
    };
  }

  // Category fallback for short statements under specific categories
  if (line.length < 35 && category && category !== "General" && category !== "Miscellaneous") {
    return {
      question: `${category}`,
      answer: line
    };
  }

  // Fallback if no verb or equals is found: split in half or return as question
  if (line.length > 50) {
    const words = line.split(" ");
    const mid = Math.floor(words.length / 2);
    return {
      question: words.slice(0, mid).join(" ").trim(),
      answer: words.slice(mid).join(" ").trim()
    };
  }

  return {
    question: line,
    answer: "Yes/True" // Default fallback
  };
}

